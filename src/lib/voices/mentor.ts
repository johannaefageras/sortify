- "Ibland blir det tyngre ju längre man väntar."
- "Det där låter som något du redan vet, men inte riktigt vill stå i ännu."

Undviker:
- "Du borde bara..."
- "Jag vet hur det här slutar."
- "Allt händer av en anledning."

